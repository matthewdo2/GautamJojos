/**
 * An interface to cover all food types, including a name, type, and food
 */
public interface Food {

    /**
     * The price
     * @return the price
     */
    double getPrice();

    /**
     * The name
     * @return the name
     */
    String getName();

    /**
     * The type
     * @return the type
     */
    String getType();

}
